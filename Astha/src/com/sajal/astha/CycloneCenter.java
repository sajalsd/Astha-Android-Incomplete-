package com.sajal.astha;

public class CycloneCenter {

	public String cc_name;
	public String cc_phone;
	public String cc_district;
	public String cc_Ps;
	public String cc_union;
	public String cc_vill;
	public String cc_alt_use;
	
	
	public CycloneCenter(String cc_name, String cc_phone, String cc_district,
			String cc_Ps, String cc_union, String cc_vill, String cc_alt_use) {
		super();
		this.cc_name = cc_name;
		this.cc_phone = cc_phone;
		this.cc_district = cc_district;
		this.cc_Ps = cc_Ps;
		this.cc_union = cc_union;
		this.cc_vill = cc_vill;
		this.cc_alt_use = cc_alt_use;
	}
	public String getCc_name() {
		return cc_name;
	}
	public void setCc_name(String cc_name) {
		this.cc_name = cc_name;
	}
	public String getCc_phone() {
		return cc_phone;
	}
	public void setCc_phone(String cc_phone) {
		this.cc_phone = cc_phone;
	}
	public String getCc_district() {
		return cc_district;
	}
	public void setCc_district(String cc_district) {
		this.cc_district = cc_district;
	}
	public String getCc_Ps() {
		return cc_Ps;
	}
	public void setCc_Ps(String cc_Ps) {
		this.cc_Ps = cc_Ps;
	}
	public String getCc_union() {
		return cc_union;
	}
	public void setCc_union(String cc_union) {
		this.cc_union = cc_union;
	}
	public String getCc_vill() {
		return cc_vill;
	}
	public void setCc_vill(String cc_vill) {
		this.cc_vill = cc_vill;
	}
	public String getCc_alt_use() {
		return cc_alt_use;
	}
	public void setCc_alt_use(String cc_alt_use) {
		this.cc_alt_use = cc_alt_use;
	}
	
	

}
